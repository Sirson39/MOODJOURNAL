        public async Task<List<Category>> GetCategoriesAsync(int userId)
        {
            await EnsureInitializedAsync();

            await using var conn = await _dataSource.OpenConnectionAsync();
            await using var cmd = conn.CreateCommand();
            cmd.CommandText = """
                SELECT id, user_id, name, color, is_default, created_at
                FROM categories
                WHERE user_id = @user_id OR user_id = 0
                ORDER BY is_default DESC, name ASC;
                """;
            cmd.Parameters.AddWithValue("user_id", userId);

            var categories = new List<Category>();
            await using var reader = await cmd.ExecuteReaderAsync();
            while (await reader.ReadAsync())
            {
                categories.Add(MapCategory(reader));
            }

            return categories;
        }

        public async Task<int> SaveCategoryAsync(Category category)
        {
            await EnsureInitializedAsync();

            await using var conn = await _dataSource.OpenConnectionAsync();

            if (category.Id != 0)
            {
                await using var updateCmd = conn.CreateCommand();
                updateCmd.CommandText = """
                    UPDATE categories
                    SET user_id = @user_id,
                        name = @name,
                        color = @color,
                        is_default = @is_default,
                        created_at = @created_at
                    WHERE id = @id;
                    """;
                updateCmd.Parameters.AddWithValue("id", category.Id);
                updateCmd.Parameters.AddWithValue("user_id", category.UserId);
                updateCmd.Parameters.AddWithValue("name", category.Name);
                updateCmd.Parameters.AddWithValue("color", category.Color);
                updateCmd.Parameters.AddWithValue("is_default", category.IsDefault);
                updateCmd.Parameters.AddWithValue("created_at", category.CreatedAt);

                return await updateCmd.ExecuteNonQueryAsync();
            }

            category.CreatedAt = DateTime.Now;

            await using var insertCmd = conn.CreateCommand();
            insertCmd.CommandText = """
                INSERT INTO categories (user_id, name, color, is_default, created_at)
                VALUES (@user_id, @name, @color, @is_default, @created_at)
                RETURNING id;
                """;
            insertCmd.Parameters.AddWithValue("user_id", category.UserId);
            insertCmd.Parameters.AddWithValue("name", category.Name);
            insertCmd.Parameters.AddWithValue("color", category.Color);
            insertCmd.Parameters.AddWithValue("is_default", category.IsDefault);
            insertCmd.Parameters.AddWithValue("created_at", category.CreatedAt);

            var newId = (int)(await insertCmd.ExecuteScalarAsync() ?? 0);
            category.Id = newId;
            return newId;
        }

